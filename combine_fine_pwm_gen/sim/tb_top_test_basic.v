`timescale 1ns / 1ps

//==============================================================================
// 基础顶层测试平台
// 简单启动顶层模块并观察基本连接
//==============================================================================

module tb_top_test_basic;

// 时钟和复位信号
reg sys_clk;
reg sys_rst_n;

// 顶层模块实例化
top_test uut (
    .sys_clk(sys_clk),
    .sys_rst_n(sys_rst_n)
);

//==============================================================================
// 时钟生成 - 50MHz
//==============================================================================
initial begin
    sys_clk = 1'b0;
    forever #10 sys_clk = ~sys_clk;  // 20ns周期 = 50MHz
end

//==============================================================================
// 测试激励
//==============================================================================
initial begin
    // 初始复位
    sys_rst_n = 1'b0;

    $display("=================================================================");
    $display("顶层模块连接测试开始");
    $display("=================================================================");

    // 保持复位100ns
    #100;

    // 释放复位
    $display("[%0t] 释放系统复位", $time);
    sys_rst_n = 1'b1;

    // 等待系统稳定
    #500;

    $display("[%0t] 系统运行中...", $time);

    // 运行足够时间观察行为
    #20000;  // 20us

    $display("=================================================================");
    $display("测试完成");
    $display("=================================================================");

    $finish;
end

//==============================================================================
// 基本监控
//==============================================================================

// 监控MMCM锁定
always @(uut.mmcm_locked) begin
    if (uut.mmcm_locked)
        $display("[%0t] MMCM已锁定", $time);
end

// 监控TDC输出
always @(posedge uut.valid_out) begin
    $display("[%0t] TDC输出: A=%d, B=%d",
             $time, uut.fine_count_a, uut.fine_count_b);
end

// 监控复位状态
wire rst_n = uut.rst_n;
wire tdc_rst_n = uut.tdc_rst_n;

always @(rst_n or tdc_rst_n) begin
    $display("[%0t] 复位状态: rst_n=%b, tdc_rst_n=%b",
             $time, rst_n, tdc_rst_n);
end

//==============================================================================
// 连接性手动检查说明
//==============================================================================
/*
在仿真运行时，请检查以下信号是否都有效（不是x或z）：

1. uut.clk_400m          - MMCM输出的400MHz时钟
2. uut.clk_400m_144deg   - MMCM输出的400MHz 144度时钟
3. uut.pwm_out           - PWM生成器输出
4. uut.fine_count_a      - TDC输出A
5. uut.fine_count_b      - TDC输出B
6. uut.valid_out         - TDC有效信号

如果这些信号都有效，说明模块间基本连接正常。

详细连接检查：
- uut.u_mmcm.clk_out1 应该等于 uut.clk_400m
- uut.u_mmcm.clk_out2 应该等于 uut.clk_400m_144deg
- uut.u_pwm_gen.clk_400m_144deg 应该等于 uut.clk_400m_144deg
- uut.u_fine_counter.clk_400m 应该等于 uut.clk_400m
- uut.u_fine_counter.pwm_signal 应该等于 uut.pwm_out

*/

//==============================================================================
// 超时保护
//==============================================================================
initial begin
    #50000;  // 50us后自动结束
    $display("[%0t] 仿真超时结束", $time);
    $finish;
end

endmodule