// ===========================================================================
// 时间戳合成器 - 合并粗计数和细计数结果
// 功能: 将粗计数器和细计数器的结果合成为最终时间间隔
// 输入: 粗计数值 + 细计数值
// 输出: 高精度时间间隔
// ===========================================================================

module timestamp_synthesizer #(
    parameter COARSE_WIDTH = 32,      // 粗计数位宽
    parameter FINE_WIDTH = 7,         // 细计数位宽 (修正: 6->7)
    parameter COARSE_PERIOD = 2.5,    // 粗计数周期(ns)
    parameter FINE_PERIOD = 34.7      // 细计数周期(ns) (修正: 41.7->34.7)
)(
    input wire [COARSE_WIDTH-1:0] start_coarse_ts,  // START时刻粗时间戳
    input wire [COARSE_WIDTH-1:0] stop_coarse_ts,   // STOP时刻粗时间戳
    input wire [FINE_WIDTH-1:0] fine_count,         // 细计数值
    output wire [COARSE_WIDTH+FINE_WIDTH-1:0] timestamp // 合成时间戳
);

    // =========================================================================
    // 参数计算
    // =========================================================================

    // 时间计算常数
    localparam COARSE_NS = COARSE_PERIOD;                    // 2.5ns
    localparam FINE_NS = FINE_PERIOD;                        // 41.7ps
    localparam FINE_SCALE = 1000 / FINE_NS;                  // 细计数缩放因子
    localparam TIMESTAMP_WIDTH = COARSE_WIDTH + FINE_WIDTH;  // 总位宽

    // =========================================================================
    // 内部信号定义
    // =========================================================================

    // 粗计数时间计算 (定点数表示)
    wire [63:0] coarse_time_ns;      // 粗计数时间(ns)

    // 细计数时间计算 (定点数表示)
    wire [31:0] fine_time_ps;        // 细计数时间(ps)
    wire [31:0] fine_time_ns_scaled; // 细计数时间(缩放后)

    // 合成时间
    wire [63:0] total_time_scaled;   // 总时间(缩放后)

    // =========================================================================
    // 时间计算实现
    // =========================================================================

    // 1. 粗计数时间差计算
    // 时间差 = (STOP时间戳 - START时间戳) × 2.5ns
    wire [COARSE_WIDTH-1:0] coarse_diff;
    assign coarse_diff = stop_coarse_ts - start_coarse_ts;
    assign coarse_time_ns = coarse_diff * COARSE_NS;

    // 2. 细计数时间计算
    // 细计数 × 41.7ps = 细计数时间(ps)
    assign fine_time_ps = fine_count * FINE_NS;

    // 3. 细计数时间单位转换 (ps -> 缩放ns)
    // 为了保持精度，使用定点数运算
    assign fine_time_ns_scaled = fine_time_ps * 1000 / FINE_SCALE;

    // 4. 时间合成
    // 总时间 = 粗计数时间 + 细计数时间
    assign total_time_scaled = coarse_time_ns * FINE_SCALE + fine_time_ns_scaled;

    // =========================================================================
    // 输出格式化
    // =========================================================================

    // 将缩放后的时间转换回标准格式
    // 取高COARSE_WIDTH+FINE_WIDTH位作为输出
    assign timestamp = total_time_scaled[TIMESTAMP_WIDTH-1:0];

    // =========================================================================
    // 定点数精度说明
    // =========================================================================

    // 为了在硬件中实现高精度计算，使用定点数表示:
    //
    // 时间表示格式:
    // [63:32] - 整数部分 (ns)
    // [31:0]  - 小数部分 (ps/缩放因子)
    //
    // 例如，测量结果为123.456ns:
    // - 整数部分: 123
    // - 小数部分: 456ps
    // - 定点数表示: 123 × 缩放因子 + 456/缩放因子

    // =========================================================================
    // 简化实现 (备选方案)
    // =========================================================================

    // 如果不需要极高的精度，可以使用简化的定点数实现:
    /*
    // 简化的时间合成 (直接使用位拼接)
    // 这种方法假设细计数已经代表了正确的分数部分
    assign timestamp = {coarse_count, fine_count};

    // 或者使用定点数乘法
    // 时间 = 粗计数 × 2.5ns + 细计数 × 0.0417ns
    // 使用Q格式定点数: Q16.16格式
    localparam COARSE_FACTOR = 32'h28000000;  // 2.5的Q16.16表示
    localparam FINE_FACTOR = 32'h00AAAAAB;    // 0.0417的Q16.16表示

    wire [63:0] coarse_part_q;
    wire [63:0] fine_part_q;
    wire [63:0] total_q;

    assign coarse_part_q = coarse_count * COARSE_FACTOR;
    assign fine_part_q = fine_count * FINE_FACTOR;
    assign total_q = coarse_part_q + fine_part_q;

    assign timestamp = total_q[47:10];  // 取合适的位宽
    */

    // =========================================================================
    // 精度分析
    // =========================================================================

    // 理论精度:
    // - 粗计数精度: 2.5ns
    // - 细计数精度: 41.7ps
    // - 总精度: 41.7ps
    //
    // 实际精度 (考虑硬件因素):
    // - CARRY4延迟变化: ±5%
    // - 时钟抖动: ±20ps
    // - 实际精度: 50-100ps

    // =========================================================================
    // 资源使用分析
    // =========================================================================

    // 乘法器资源:
    // - 粗计数乘法器: 32位 × 常数
    // - 细计数乘法器: 6位 × 常数
    // - 综合工具会优化常数乘法
    //
    // 预计资源使用:
    // - LUT: 约100-200个
    // - FF: 约60-80个
    // - DSP: 0个 (常数乘法被优化为LUT)

    // =========================================================================
    // 时序约束建议
    // =========================================================================

    // set_max_delay 2.0 -from [get_ports coarse_count] -to [get_ports timestamp]
    // set_max_delay 2.0 -from [get_ports fine_count] -to [get_ports timestamp]
    // set_multicycle_path 2 -setup -from [get_registers *coarse_count*] -to [get_registers *timestamp*]

    // =========================================================================
    // 测试用例
    // =========================================================================

    // 测试场景1: 短时间测量
    // coarse_count = 1, fine_count = 25
    // 理论时间 = 1 × 2.5ns + 25 × 0.0417ns = 2.5ns + 1.0425ns = 3.5425ns
    //
    // 测试场景2: 长时间测量
    // coarse_count = 1000, fine_count = 40
    // 理论时间 = 1000 × 2.5ns + 40 × 0.0417ns = 2500ns + 1.668ns = 2501.668ns

endmodule

// ===========================================================================
// 设计优化说明:
//
// 1. 为什么使用定点数计算:
//    - 硬件中没有浮点运算单元
    //    - 定点数计算速度快、资源少
    //    - 可以满足精度要求
    //
// 2. 为什么选择这种时间合成方式:
//    - 保持粗计数的大范围测量能力
    //    - 利用细计数的高精度特性
    //    - 通过定点数保持足够的精度
    //
// 3. 精度与资源的平衡:
    //    - 使用足够宽的位宽保持精度
    //    - 通过常数优化减少资源使用
    //    - 确保在400MHz时钟下满足时序要求
// ===========================================================================