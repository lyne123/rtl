// ===========================================================================
// CARRY4 TDC测试平台 - 验证基于CARRY4的TDC功能
// 测试目标: 验证PWM输入、边沿检测、粗计数和细计数功能
// ===========================================================================

`timescale 1ns / 1ps

module tb_carry4_tdc;

    // =========================================================================
    // 测试平台参数
    // =========================================================================

    // 时钟参数
    parameter CLK_50M_PERIOD = 20;    // 50MHz时钟周期(ns)
    parameter CLK_400M_PERIOD = 2.5;  // 400MHz时钟周期(ns)

    // 测试参数
    parameter TEST_PWM_FREQ = 1_000_000;  // 1MHz PWM测试频率
    parameter TEST_PWM_DUTY = 50;         // 50%占空比
    parameter SIMULATION_TIME = 50000;    // 仿真时间(ns)

    // 调试参数

    // =========================================================================
    // 信号声明
    // =========================================================================

    // 系统信号
    reg clk_50m = 1'b0;       // 50MHz系统时钟 (直接初始化)
    reg rst_n = 1'b0;         // 异步复位信号 (直接初始化)

    // TDC输入信号
    reg pwm_in = 1'b0;        // PWM输入信号 (直接初始化)
    wire [37:0] time_interval; // 时间间隔测量值
    wire valid;               // 数据有效信号

    // 测试控制信号
    integer test_count;      // 测试计数器

    // =========================================================================
    // TDC模块实例化
    // =========================================================================

    tdc_top_carry4 uut (
        .clk_50m(clk_50m),
        .rst_n(rst_n),
        .pwm_in(pwm_in),
        .time_interval(time_interval),
        .valid(valid)
    );

    // =========================================================================
    // 时钟生成
    // =========================================================================

    // 50MHz时钟生成 (确保时钟稳定)
    always #(CLK_50M_PERIOD/2) clk_50m = ~clk_50m;

    // =========================================================================
    // 测试程序控制
    // =========================================================================

    initial begin
        // 初始化信号
        test_count = 0;

        // 初始化调试

        // 显示测试开始信息
        $display("==========================================");
        $display("CARRY4 TDC 测试程序开始");
        $display("目标芯片: xc7a100tfgg484-2");
        $display("测量精度: 50ps分辨率");
        $display("仿真时间: %0d ns", SIMULATION_TIME);
        $display("==========================================");

        // 复位阶段
        $display("[T=%0t] 系统复位开始...", $time);
        #1000;   // 等待复位50ns
        rst_n = 1'b1;
        $display("[T=%0t] 系统复位完成", $time);

        // 等待MMCM锁定和系统时钟
        $display("[T=%0t] 等待MMCM锁定和系统时钟...", $time);
        #2000;  // 等待MMCM时钟
        $display("[T=%0t] MMCM锁定，系统时钟稳定", $time);

        // 开始测试
        $display("[T=%0t] 开始PWM信号测试", $time);

        // 运行PWM测试
        run_pwm_tests();

        // 结束测试
        #1000;
        $display("==========================================");
        $display("CARRY4 TDC 测试程序结束");
        $display("==========================================");

        $finish;
    end

    // =========================================================================
    // PWM信号测试任务
    // =========================================================================

    task run_pwm_tests;
        begin
            // 测试1: 窄脉冲测试 (10ns)
            $display("[T=%0t] 测试1: 窄脉冲测试 (10ns)", $time);
            generate_pwm_pulse(10);

            // 等待测量结果
            @(posedge valid);
            #10;
            display_measurement_result();

            // 测试2: 中脉冲测试 (100ns)
            $display("[T=%0t] 测试2: 中脉冲测试 (100ns)", $time);
            generate_pwm_pulse(100);

            // 等待测量结果
            @(posedge valid);
            #10;
            display_measurement_result();

            // 测试3: 宽脉冲测试 (1000ns)
            $display("[T=%0t] 测试3: 宽脉冲测试 (1000ns)", $time);
            generate_pwm_pulse(1000);

            // 等待测量结果
            @(posedge valid);
            #10;
            display_measurement_result();

            // 测试4: 连续PWM信号测试
            $display("[T=%0t] 测试4: 连续PWM信号测试", $time);
            generate_continuous_pwm(100, 50); // 100ns周期，50%占空比

            // 收集多个测量结果
            repeat(5) begin
                @(posedge valid);
                #10;
                display_measurement_result();
            end
        end
    endtask

    // =========================================================================
    // PWM信号生成任务
    // =========================================================================

    // 生成单脉冲PWM
    task generate_pwm_pulse;
        input [31:0] pulse_width;  // 脉冲宽度(ns)
        begin
            // 等待当前PWM信号结束
            wait(pwm_in == 1'b0);

            // 生成脉冲
            pwm_in = 1'b1;
            #pulse_width;
            pwm_in = 1'b0;

            $display("[T=%0t] 生成PWM脉冲: 宽度=%0dns", $time, pulse_width);
        end
    endtask

    // 生成连续PWM信号
    task generate_continuous_pwm;
        input [31:0] period;       // PWM周期(ns)
        input [7:0] duty_cycle;    // 占空比(%)
        integer high_time, low_time;
        begin
            high_time = (period * duty_cycle) / 100;
            low_time = period - high_time;

            $display("[T=%0t] 生成连续PWM: 周期=%0dns, 高电平=%0dns, 低电平=%0dns",
                     $time, period, high_time, low_time);

            // 生成3个完整的PWM周期
            repeat(3) begin
                pwm_in = 1'b1;
                #high_time;
                pwm_in = 1'b0;
                #low_time;
            end
        end
    endtask

    // =========================================================================
    // 测量结果显示任务
    // =========================================================================

    task display_measurement_result;
        begin
            if (valid) begin
                $display("[T=%0t] 测量结果: time_interval=%0d, valid=%0b",
                         $time, time_interval, valid);

                // 写入测量日志

                // 计算实际时间值
                // 注意: 测量结果需要根据实际时间戳计算原始时间值
                // 实际测量时间需要根据PWM信号的实际时间计算

                test_count = test_count + 1;
                $display("[T=%0t] 测试计数: %0d", $time, test_count);
            end else begin
                $display("[T=%0t] 错误: 测量数据无效", $time);
            end
        end
    endtask

    // =========================================================================
    // 波形调试
    // =========================================================================

    initial begin
        // 生成VCD波形文件
        $dumpfile("tb_carry4_tdc.vcd");
        $dumpvars(0, tb_carry4_tdc);

        // 详细信号记录
        $dumpvars(1, uut);  // 记录顶层模块
        $dumpvars(1, uut.u_mmcm);  // 记录MMCM模块
        $dumpvars(1, uut.u_edge_sync);  // 记录边沿检测模块
        $dumpvars(1, uut.u_coarse_counter);  // 记录粗计数器
        $dumpvars(1, uut.u_tdc_a);  // 记录TDC-A模块
        $dumpvars(1, uut.u_tdc_b);  // 记录TDC-B模块
    end

    // =========================================================================
    // 测试监控
    // =========================================================================

    // 超时监控
    initial begin
        #SIMULATION_TIME;
        $display("[T=%0t] 错误: 仿真超时", $time);
        $finish;
    end

    // 实时数据显示
    always @(posedge valid) begin
        $display("[T=%0t] 测量结果: 时间间隔 = %0d (单位)",
                 $time, time_interval);
    end

    // =========================================================================
    // 测试验证点
    // =========================================================================

    // 验证点1: 复位功能
    // - 复位期间输出信号应为0
    // - 复位后系统应能正常启动

    // 验证点2: MMCM功能
    // - MMCM应能锁定并生成稳定时钟
    // - 输出400MHz时钟应能稳定

    // 验证点3: 边沿检测
    // - PWM信号上升沿应能正确检测
    // - PWM信号下降沿应能正确检测
    // - 边沿检测延迟应在10ns内

    // 验证点4: 粗计数
    // - 粗计数值应跟随PWM信号变化
    // - 粗计数值应在PWM高电平时计数
    // - 计数值应与实际时间误差小

    // 验证点5: 细计数
    // - CARRY4延迟链应能正确计数
    // - 细计数值应能正确反映延迟
    // - 细计数值应与时钟边沿对齐

    // 验证点6: 时间戳合成
    // - 粗计数和细计数应能正确组合
    // - 测量时间间隔应与PWM匹配
    // - 测量结果应能达到预期精度

    // =========================================================================
    // 预期测试结果
    // =========================================================================

    // 测试1 (10ns脉冲):
    // - 测量计数值: ~10ns
    // - 实际精度: 约100ps

    // 测试2 (100ns脉冲):
    // - 测量计数值: ~100ns
    // - 实际精度: 约100ps

    // 测试3 (1000ns脉冲):
    // - 测量计数值: ~1000ns
    // - 实际精度: 约100ps

    // 测试4 (连续PWM):
    // - 测量值应能稳定
    // - 多次测量结果应一致

endmodule

