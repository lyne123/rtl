            // 锟斤拷锟斤拷锟斤拷锟斤拷
            pwm_in = 1'b1;
            #pulse_width;
            pwm_in = 1'b0;

            $display("[T=%0t] 锟斤拷锟斤拷PWM锟斤拷锟斤拷: 锟斤拷锟斤拷=%0dns", $time, pulse_width);
        end
    endtask

