// ===========================================================================
// CARRY4 TDC测试平台 - 验证基于CARRY4的TDC功能
// 测试目标: 验证PWM输入、边沿检测、粗计数、细计数和时间合成
// ===========================================================================

`timescale 1ns / 1ps

module tb_carry4_tdc;

    // =========================================================================
    // 测试平台参数
    // =========================================================================

    // 时钟参数
    parameter CLK_50M_PERIOD = 20;    // 50MHz时钟周期(ns)
    parameter CLK_400M_PERIOD = 2.5;  // 400MHz时钟周期(ns)

    // 测试参数
    parameter TEST_PWM_FREQ = 1_000_000;  // 1MHz PWM测试频率
    parameter TEST_PWM_DUTY = 50;         // 50%占空比
    parameter SIMULATION_TIME = 10000;    // 仿真时间(ns)

    // =========================================================================
    // 信号定义
    // =========================================================================

    // 系统信号
    reg clk_50m = 1'b0;       // 50MHz系统时钟 (直接初始化)
    reg rst_n = 1'b0;         // 异步复位信号 (直接初始化)

    // TDC接口信号
    reg pwm_in = 1'b0;        // PWM输入信号 (直接初始化)
    wire [37:0] time_interval; // 时间间隔测量结果
    wire valid;               // 数据有效信号

    // 测试控制信号
    integer test_count;      // 测试计数器

    // =========================================================================
    // TDC模块实例化
    // =========================================================================

    tdc_top_carry4 uut (
        .clk_50m(clk_50m),
        .rst_n(rst_n),
        .pwm_in(pwm_in),
        .time_interval(time_interval),
        .valid(valid)
    );

    // =========================================================================
    // 时钟生成
    // =========================================================================

    // 50MHz时钟生成 (确保立即开始)
    always #(CLK_50M_PERIOD/2) clk_50m = ~clk_50m;

    // =========================================================================
    // 测试流程控制
    // =========================================================================

    initial begin
        // 初始化信号
        test_count = 0;

        // 显示测试开始信息
        $display("==========================================");
        $display("CARRY4 TDC 功能测试开始");
        $display("测试芯片: xc7a100tfgg484-2");
        $display("设计目标: 50ps精度");
        $display("测试时间: %0d ns", SIMULATION_TIME);
        $display("==========================================");

        // 复位阶段
        $display("[T=%0t] 系统复位开始...", $time);
        #50;   // 保持复位50ns
        rst_n = 1'b1;
        $display("[T=%0t] 系统复位结束", $time);

        // 等待MMCM锁定和系统稳定
        $display("[T=%0t] 等待MMCM锁定和系统稳定...", $time);
        #500;  // 等待MMCM稳定
        $display("[T=%0t] MMCM锁定完成，系统稳定", $time);

        // 开始测试
        $display("[T=%0t] 开始PWM测试", $time);

        // 运行测试
        run_pwm_tests();

        // 结束测试
        #1000;
        $display("==========================================");
        $display("CARRY4 TDC 功能测试完成");
        $display("==========================================");
        $finish;
    end

    // =========================================================================
    // PWM测试任务
    // =========================================================================

    task run_pwm_tests;
        begin
            // 测试1: 短脉冲测试 (10ns)
            $display("[T=%0t] 测试1: 短脉冲测试 (10ns)", $time);
            generate_pwm_pulse(10);

            // 等待测量完成
            @(posedge valid);
            #10;
            display_measurement_result();

            // 测试2: 中等脉冲测试 (100ns)
            $display("[T=%0t] 测试2: 中等脉冲测试 (100ns)", $time);
            generate_pwm_pulse(100);

            // 等待测量完成
            @(posedge valid);
            #10;
            display_measurement_result();

            // 测试3: 长脉冲测试 (1000ns)
            $display("[T=%0t] 测试3: 长脉冲测试 (1000ns)", $time);
            generate_pwm_pulse(1000);

            // 等待测量完成
            @(posedge valid);
            #10;
            display_measurement_result();

            // 测试4: 连续PWM测试
            $display("[T=%0t] 测试4: 连续PWM测试", $time);
            generate_continuous_pwm(100, 50); // 100ns周期，50%占空比

            // 采集多个测量结果
            repeat(5) begin
                @(posedge valid);
                #10;
                display_measurement_result();
            end
        end
    endtask

    // =========================================================================
    // PWM信号生成任务
    // =========================================================================

    // 生成单个PWM脉冲
    task generate_pwm_pulse;
        input [31:0] pulse_width;  // 脉冲宽度(ns)
        begin
            // 等待当前PWM信号结束
            wait(pwm_in == 1'b0);

            // 生成脉冲
            pwm_in = 1'b1;
            #pulse_width;
            pwm_in = 1'b0;

            $display("[T=%0t] 生成PWM脉冲: 宽度=%0dns", $time, pulse_width);
        end
    endtask

    // 生成连续PWM信号
    task generate_continuous_pwm;
        input [31:0] period;       // PWM周期(ns)
        input [7:0] duty_cycle;    // 占空比(%)
        begin
            integer high_time, low_time;
            high_time = (period * duty_cycle) / 100;
            low_time = period - high_time;

            $display("[T=%0t] 生成连续PWM: 周期=%0dns, 高电平=%0dns, 低电平=%0dns",
                     $time, period, high_time, low_time);

            // 生成3个完整周期
            repeat(3) begin
                pwm_in = 1'b1;
                #high_time;
                pwm_in = 1'b0;
                #low_time;
            end
        end
    endtask

    // =========================================================================
    // 结果显示任务
    // =========================================================================

    task display_measurement_result;
        begin
            if (valid) begin
                $display("[T=%0t] 测量结果: time_interval=%0d, valid=%0b",
                         $time, time_interval, valid);

                // 理论值计算和误差分析
                // 注意: 这里需要根据实际测量原理计算理论值
                // 实际测试中需要根据输入PWM参数计算期望值

                test_count = test_count + 1;
                $display("[T=%0t] 测试计数: %0d", $time, test_count);
            end else begin
                $display("[T=%0t] 警告: 测量结果无效", $time);
            end
        end
    endtask

    // =========================================================================
    // 波形生成
    // =========================================================================

    initial begin
        // 生成VCD波形文件
        $dumpfile("tb_carry4_tdc.vcd");
        $dumpvars(0, tb_carry4_tdc);

        // 设置波形记录级别
        $dumpvars(1, uut);  // 记录顶层模块
        $dumpvars(1, uut.u_mmcm);  // 记录MMCM模块
        $dumpvars(1, uut.u_edge_sync);  // 记录边沿检测模块
        $dumpvars(1, uut.u_coarse_counter);  // 记录粗计数器
        $dumpvars(1, uut.u_carry4_chain);  // 记录CARRY4延迟链
    end

    // =========================================================================
    // 仿真监控
    // =========================================================================

    // 超时监控
    initial begin
        #SIMULATION_TIME;
        $display("[T=%0t] 警告: 仿真超时", $time);
        $finish;
    end

    // 实时信号监控
    always @(posedge valid) begin
        $display("[T=%0t] 测量完成: 时间间隔 = %0d (单位)",
                 $time, time_interval);
    end

    // =========================================================================
    // 测试验证点
    // =========================================================================

    // 验证点1: 复位功能
    // - 复位期间所有信号应为0
    // - 复位释放后系统应正常启动

    // 验证点2: MMCM锁定
    // - MMCM应在合理时间内锁定
    // - 锁定后400MHz时钟应稳定

    // 验证点3: 边沿检测
    // - PWM上升沿应正确检测
    // - PWM下降沿应正确检测
    // - 边沿检测延迟应小于10ns

    // 验证点4: 粗计数
    // - 粗计数器应在PWM上升沿启动
    // - 粗计数器应在PWM下降沿停止
    // - 计数值应与实际时间成比例

    // 验证点5: 细计数
    // - CARRY4延迟链应正确工作
    // - 温度计码应正确生成
    // - 细计数应与延迟时间成比例

    // 验证点6: 时间合成
    // - 粗计数和细计数应正确合成
    // - 最终时间间隔应与输入PWM匹配
    // - 测量精度应达到设计目标

    // =========================================================================
    // 预期测试结果
    // =========================================================================

    // 测试1 (10ns脉冲):
    // - 期望测量值: ~10ns
    // - 实际精度: ±100ps

    // 测试2 (100ns脉冲):
    // - 期望测量值: ~100ns
    // - 实际精度: ±100ps

    // 测试3 (1000ns脉冲):
    // - 期望测量值: ~1000ns
    // - 实际精度: ±100ps

    // 测试4 (连续PWM):
    // - 测量值应保持稳定
    // - 多次测量结果应一致

endmodule

