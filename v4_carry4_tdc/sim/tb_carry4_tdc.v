            $display("[T=%0t] 锟斤拷锟斤拷1: 锟斤拷锟斤拷锟斤拷锟斤拷锟� (10ns)", $time);
            generate_pwm_pulse(10);

            // 锟饺达拷锟斤拷锟斤拷锟斤拷锟�
