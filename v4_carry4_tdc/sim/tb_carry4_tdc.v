        input [31:0] period;       // PWM锟斤拷锟斤拷(ns)
        input [7:0] duty_cycle;    // 占锟秸憋拷(%)
        integer high_time, low_time;
        begin
            high_time = (period * duty_cycle) / 100;
            low_time = period - high_time;

            $display("[T=%0t] 锟斤拷锟斤拷锟斤拷锟斤拷PWM: 锟斤拷锟斤拷=%0dns, 锟竭碉拷平=%0dns, 锟酵碉拷平=%0dns",
                     $time, period, high_time, low_time);

            // 锟斤拷锟斤拷3锟斤拷锟斤拷锟斤拷锟斤拷锟斤拷
            repeat(3) begin
                pwm_in = 1'b1;
                #high_time;
                pwm_in = 1'b0;
                #low_time;
            end
        end
    endtask

