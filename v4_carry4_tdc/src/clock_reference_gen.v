// ===========================================================================
// 时钟参考信号生成模块（简化版本）
// 为TDC-A和TDC-B生成下一时钟边沿参考信号
// TDC-A: 测量PWM上升沿到下一时钟边沿的时间(c)
// TDC-B: 测量PWM下降沿到下一时钟边沿的时间(d)
// ===========================================================================

module clock_reference_gen (
    input wire clk_400m,          // 400MHz时钟
    input wire rst_n,             // 异步复位
    input wire start_sync,        // 同步后的PWM上升沿
    input wire stop_sync,         // 同步后的PWM下降沿
    output reg tdc_a_ref,         // TDC-A参考信号（下一时钟边沿）
    output reg tdc_b_ref          // TDC-B参考信号（下一时钟边沿）
);

    // =========================================================================
    // 内部信号
    // =========================================================================

    reg start_sync_dly;
    reg stop_sync_dly;

    // =========================================================================
    // 信号延迟
    // =========================================================================

    always @(posedge clk_400m or negedge rst_n) begin
        if (!rst_n) begin
            start_sync_dly <= 1'b0;
            stop_sync_dly <= 1'b0;
        end else begin
            start_sync_dly <= start_sync;
            stop_sync_dly <= stop_sync;
        end
    end


    // =========================================================================
    // TDC-A参考生成
    // 生成PWM上升沿后的下一个时钟边沿信号
    // =========================================================================

    always @(posedge clk_400m or negedge rst_n) begin
        if (!rst_n) begin
            tdc_a_ref <= 1'b0;
        end else begin
            // 使用延迟信号，确保在PWM上升沿后的下一个时钟
            tdc_a_ref <= start_sync_dly;
        end
    end

    // =========================================================================
    // TDC-B参考生成
    // 生成PWM下降沿后的下一个时钟边沿信号
    // =========================================================================

    always @(posedge clk_400m or negedge rst_n) begin
        if (!rst_n) begin
            tdc_b_ref <= 1'b0;
        end else begin
            // 使用延迟信号，确保在PWM下降沿后的下一个时钟
            tdc_b_ref <= stop_sync_dly;
        end
    end

    // =========================================================================
    // 时序说明
    // =========================================================================
    //
    // TDC-A时序 (新方法):
    // PWM上升沿:     ___|‾‾‾|________________
    // tdc_a_ref:     _______|‾|_______________
    //                ↑       ↑
    //            PWM上升沿  下一时钟参考
    //
    // TDC-B时序 (新方法):
    // PWM下降沿:     ________________|‾‾‾|___
    // tdc_b_ref:     _______________|‾|______
    //                                ↑
    //                            下一时钟参考
    //
    // 测量目标:
    // c = PWM上升沿到tdc_a_ref的时间
    // d = PWM下降沿到tdc_b_ref的时间
    //

endmodule