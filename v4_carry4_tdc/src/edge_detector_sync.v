// ===========================================================================
// 边沿检测与同步模块 - 处理PWM输入信号
// 功能: 检测PWM上升沿和下降沿，提供同步输出信号
// 设计: 三级同步器 + 边沿检测逻辑
// ===========================================================================

module edge_detector_sync (
    input wire clk_400m,          // 400MHz系统时钟
    input wire rst_n,             // 异步复位信号
    input wire pwm_in,            // PWM信号输入
    output wire start_edge,       // PWM上升沿检测输出
    output wire stop_edge,        // PWM下降沿检测输出
    output wire start_sync,       // 同步后的start信号
    output wire stop_sync         // 同步后的stop信号
);

    // =========================================================================
    // 内部信号定义
    // =========================================================================

    // PWM信号同步链 (三级同步器)
    reg [2:0] pwm_sync_reg;

    // 边沿检测寄存器
    reg pwm_sync_d1;

    // 输出信号寄存器
    reg start_edge_reg;
    reg stop_edge_reg;
    reg start_sync_reg;
    reg stop_sync_reg;

    // =========================================================================
    // 三级同步器 - 消除亚稳态
    // =========================================================================

    always @(posedge clk_400m or negedge rst_n) begin
        if (!rst_n) begin
            pwm_sync_reg <= 3'b000;
        end else begin
            // 三级移位寄存器同步
            pwm_sync_reg <= {pwm_sync_reg[1:0], pwm_in};
        end
    end

    // 同步后的PWM信号 (第三级输出)
    wire pwm_synced = pwm_sync_reg[2];

    // =========================================================================
    // 边沿检测逻辑
    // =========================================================================

    always @(posedge clk_400m or negedge rst_n) begin
        if (!rst_n) begin
            pwm_sync_d1 <= 1'b0;
            start_edge_reg <= 1'b0;
            stop_edge_reg <= 1'b0;
        end else begin
            // 延迟一个周期用于边沿检测
            pwm_sync_d1 <= pwm_synced;

            // 上升沿检测: 当前为1且前一个周期为0
            start_edge_reg <= pwm_synced && !pwm_sync_d1;

            // 下降沿检测: 当前为0且前一个周期为1
            stop_edge_reg <= !pwm_synced && pwm_sync_d1;
        end
    end

    // =========================================================================
    // 同步输出信号生成
    // =========================================================================

    always @(posedge clk_400m or negedge rst_n) begin
        if (!rst_n) begin
            start_sync_reg <= 1'b0;
            stop_sync_reg <= 1'b0;
        end else begin
            // 同步输出信号
            start_sync_reg <= start_edge_reg;
            stop_sync_reg <= stop_edge_reg;

            // 确保信号只持续一个时钟周期
            if (start_edge_reg) begin
                start_sync_reg <= 1'b1;
            end else begin
                start_sync_reg <= 1'b0;
            end

            if (stop_edge_reg) begin
                stop_sync_reg <= 1'b1;
            end else begin
                stop_sync_reg <= 1'b0;
            end
        end
    end

    // =========================================================================
    // 输出赋值
    // =========================================================================

    assign start_edge = start_edge_reg;
    assign stop_edge = stop_edge_reg;
    assign start_sync = start_sync_reg;
    assign stop_sync = stop_sync_reg;

    // =========================================================================
    // 功能说明
    // =========================================================================

    // PWM信号处理流程:
    // 1. 原始PWM输入 -> 三级同步器 -> 同步PWM信号
    // 2. 同步PWM信号 -> 边沿检测 -> 上升沿/下降沿脉冲
    // 3. 边沿脉冲 -> 同步输出 -> TDC其他模块使用

    // 信号时序:
    // PWM_in     _______|‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾
    //            异步输入
    //
    // pwm_sync   _______________|‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾
    //            三级同步后
    //
    // start_edge _________________|‾|_______________________
    //            上升沿检测(单周期脉冲)
    //
    // stop_edge  _________________________________|‾|________
    //            下降沿检测(单周期脉冲)

    // =========================================================================
    // 设计考虑
    // =========================================================================

    // 1. 为什么需要三级同步器:
    //    - PWM信号是异步输入，可能随时变化
    //    - 相对于400MHz时钟，PWM信号是异步的
    //    - 三级同步器提供足够的亚稳态恢复时间
    //    - 失败概率: P_failure ≈ (10^-4)^3 = 10^-12

    // 2. 为什么需要边沿检测:
    //    - TDC需要精确的启动和停止时刻
    //    - 边沿检测确保每个事件只触发一次
    //    - 避免电平触发导致的重复计数

    // 3. 信号单周期脉冲设计:
    //    - 确保下游模块正确识别事件
    //    - 避免信号持续时间过长导致错误
    //    - 提高系统可靠性

    // =========================================================================
    // 时序约束建议
    // =========================================================================

    // set_false_path -from [get_ports pwm_in] -to [get_registers pwm_sync_reg*]
    // set_multicycle_path 3 -setup -from [get_registers pwm_sync_reg[0]] -to [get_registers pwm_sync_reg[2]]
    // set_max_delay 1.0 -from [get_registers pwm_sync_reg[2]] -to [get_registers start_edge_reg*]

    // =========================================================================
    // 性能参数
    // =========================================================================

    // 同步延迟: 3个时钟周期 = 7.5ns
    // 边沿检测延迟: 1个时钟周期 = 2.5ns
    // 总延迟: 4个时钟周期 = 10ns
    // 最小PWM脉宽: >10ns (确保可靠检测)
    // 最大PWM频率: <50MHz (确保可靠检测)

endmodule