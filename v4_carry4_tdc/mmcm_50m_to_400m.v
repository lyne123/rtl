// ===========================================================================
// MMCM倍频模块 - 50MHz到400MHz时钟倍频
// 使用Xilinx 7系列MMCM原语实现高质量时钟生成
// 输入: 50MHz参考时钟
// 输出: 400MHz系统时钟 (×8倍频)
// ===========================================================================

module mmcm_50m_to_400m (
    input wire clk_in_50m,       // 50MHz输入时钟
    input wire rst_n,            // 异步复位信号(低电平有效)
    output wire clk_out_400m,    // 400MHz输出时钟
    output wire locked           // MMCM锁定信号
);

    // =========================================================================
    // MMCM原语参数配置
    // =========================================================================

    // MMCM配置参数 (50MHz -> 400MHz)
    localparam CLKFBOUT_MULT_F = 16.0;    // VCO倍频系数
    localparam CLKOUT0_DIVIDE_F = 2.0;    // CLKOUT0分频系数 (16/2=8)
    localparam CLKIN1_PERIOD = 20.0;      // 输入时钟周期(ns) 1/50MHz
    localparam DIVCLK_DIVIDE = 1;         // 分频时钟分频系数

    // =========================================================================
    // 内部信号定义
    // =========================================================================

    wire clkfb_in;        // 反馈时钟输入
    wire clkfb_out;       // 反馈时钟输出
    wire clk_out_400m_buf; // 400MHz时钟(缓冲前)
    wire mmcm_reset;      // MMCM复位信号

    // =========================================================================
    // 复位信号处理
    // =========================================================================

    // MMCM复位信号 (需要高电平有效)
    assign mmcm_reset = ~rst_n;

    // =========================================================================
    // MMCM原语实例化
    // =========================================================================

    MMCME2_BASE #(
        // 时钟配置
        .BANDWIDTH("OPTIMIZED"),           // 优化模式
        .CLKFBOUT_MULT_F(CLKFBOUT_MULT_F), // VCO倍频系数
        .CLKFBOUT_PHASE(0.0),              // 反馈时钟相位
        .DIVCLK_DIVIDE(DIVCLK_DIVIDE),     // 分频时钟分频

        // 输入时钟配置
        .CLKIN1_PERIOD(CLKIN1_PERIOD),     // 输入时钟周期

        // 输出时钟0配置 (400MHz)
        .CLKOUT0_DIVIDE_F(CLKOUT0_DIVIDE_F), // 输出分频系数
        .CLKOUT0_DUTY_CYCLE(0.5),          // 占空比
        .CLKOUT0_PHASE(0.0),               // 输出相位

        // 仿真参数
        .REF_JITTER1(0.010),               // 参考时钟抖动
        .STARTUP_WAIT("FALSE")             // 启动等待

    ) mmcme2_base_inst (

        // 输入时钟
        .CLKIN1(clk_in_50m),      // 50MHz输入时钟
        .CLKFBIN(clkfb_in),       // 反馈时钟输入

        // 输出时钟
        .CLKOUT0(clk_out_400m_buf), // 400MHz输出时钟
        .CLKOUT0B(),               // 400MHz反相输出(不使用)
        .CLKOUT1(),                // 额外输出1(不使用)
        .CLKOUT1B(),               // 额外输出1反相(不使用)
        .CLKOUT2(),                // 额外输出2(不使用)
        .CLKOUT2B(),               // 额外输出2反相(不使用)
        .CLKOUT3(),                // 额外输出3(不使用)
        .CLKOUT3B(),               // 额外输出3反相(不使用)
        .CLKOUT4(),                // 额外输出4(不使用)
        .CLKOUT5(),                // 额外输出5(不使用)
        .CLKOUT6(),                // 额外输出6(不使用)

        // 反馈时钟
        .CLKFBOUT(clkfb_out),     // 反馈时钟输出
        .CLKFBOUTB(),             // 反馈时钟反相输出(不使用)

        // 控制信号
        .CLKFBSTOPPED(),          // 反馈时钟停止指示
        .CLKINSTOPPED(),          // 输入时钟停止指示
        .LOCKED(locked),          // 锁定信号
        .PWRDWN(1'b0),           // 电源关闭(不使用)
        .RST(mmcm_reset)         // 复位信号

    );

    // =========================================================================
    // 全局时钟缓冲
    // =========================================================================

    // 使用BUFG原语将MMCM输出连接到全局时钟网络
    BUFG bufg_inst (
        .O(clk_out_400m),         // 缓冲后输出
        .I(clk_out_400m_buf)      // 缓冲前输入
    );

    // 反馈时钟缓冲
    BUFG bufg_fb (
        .O(clkfb_in),             // 反馈时钟输入
        .I(clkfb_out)             // 反馈时钟输出
    );

    // =========================================================================
    // 性能参数
    // =========================================================================

    // MMCM性能指标 (xc7a100t):
    // - 输入频率范围: 19.2-800MHz
    // - 输出频率范围: 6.25-800MHz
    // - VCO频率范围: 600-1200MHz
    //
    // 本设计参数:
    // - 输入: 50MHz
    // - VCO: 50MHz × 16 = 800MHz
    // - 输出: 800MHz / 2 = 400MHz
    //
    // 时钟质量:
    // - 时钟偏斜: < 50ps
    // - 时钟抖动: < 20ps
    // - 占空比: 50%

    // =========================================================================
    // 设计考虑
    // =========================================================================

    // 1. 为什么选择MMCM而不是PLL:
    //    - MMCM提供更好的抖动性能
    //    - 支持分数倍频
    //    - 更适合高速应用
    //
    // 2. 反馈时钟设计:
    //    - 使用内部反馈确保相位对齐
    //    - 减少外部走线引入的误差
    //    - 提高时钟质量
    //
    // 3. 全局时钟缓冲:
    //    - 确保时钟信号质量
    //    - 减少时钟偏斜
    //    - 提高系统稳定性

    // =========================================================================
    // 时序约束建议
    // =========================================================================

    // create_clock -period 20.000 -name clk_50m [get_ports clk_in_50m]
    // create_clock -period 2.500 -name clk_400m [get_pins bufg_inst/O]
    // set_clock_groups -asynchronous -group {clk_50m} -group {clk_400m}

    // =========================================================================
    // 锁定信号说明
    // =========================================================================

    // locked信号行为:
    // - 0: MMCM未锁定，输出时钟不稳定
    // - 1: MMCM已锁定，输出时钟稳定
    //
    // 使用时序:
    // - 系统复位时需要等待locked信号变高
    // - 其他模块应在locked有效后才能开始工作
    // - locked信号可用于系统状态指示

endmodule

// ===========================================================================
// 使用注意事项:
//
// 1. 输入时钟质量:
//    - 确保50MHz输入时钟质量良好
    //    - 建议使用时钟缓冲器
    //    - 控制输入抖动在规格范围内
    //
// 2. 电源去耦:
    //    - MMCM对电源噪声敏感
    //    - 需要良好的电源去耦设计
    //    - 建议使用专用电源引脚
    //
// 3. 布局布线:
//    - MMCM和BUFG应该就近布局
    //    - 减少时钟走线长度
    //    - 避免时钟信号交叉干扰
// ===========================================================================