// ===========================================================================
// PWM直接延迟链采样模块 - v5版本
// 功能: 直接将PWM信号送入CARRY4延迟链，实现ps级脉宽测量
// 设计目标: 0-50ns脉宽范围，35ps分辨率
// ===========================================================================

module pwm_delay_chain_sampler (
    input wire clk_400m,           // 400MHz采样时钟
    input wire rst_n,              // 异步复位信号
    input wire pwm_in,             // PWM信号输入
    output reg [71:0] pwm_thermometer, // 72级延迟链温度计码输出
    output reg sampling_valid      // 采样有效信号
);

    // =========================================================================
    // 参数定义
    // =========================================================================

    parameter CARRY4_COUNT = 18;   // CARRY4原语数量
    parameter TOTAL_STAGES = 72;   // 总延迟级数 (CARRY4_COUNT * 4)
    parameter SINGLE_STAGE_DELAY = 35; // 单级延迟(ps)

    // =========================================================================
    // 内部信号定义
    // =========================================================================

    // 72级延迟链信号 (TOTAL_STAGES+1个节点)
    wire [TOTAL_STAGES:0] pwm_delay_chain;

    // 采样寄存器链（三级同步，减少亚稳态）
    reg [TOTAL_STAGES-1:0] sample_reg1;
    reg [TOTAL_STAGES-1:0] sample_reg2;
    reg [TOTAL_STAGES-1:0] sample_reg3;

    // 亚稳态检测信号
    reg metastable_warning;

    // =========================================================================
    // 延迟链连接
    // =========================================================================

    // 直接将PWM信号接入延迟链输入
    assign pwm_delay_chain[0] = pwm_in;

    // =========================================================================
    // CARRY4延迟链生成
    // =========================================================================

    genvar i;
    generate
        for (i = 0; i < CARRY4_COUNT; i = i + 1) begin : pwm_delay_chain_gen

            // CARRY4原语实例化 - 利用内部4级延迟
            CARRY4 carry4_inst (
                .CO(pwm_delay_chain[(i+1)*4:i*4+1]), // CO[3:0] -> 4级延迟输出
                .O(),                               // 和输出(不使用)
                .CI(pwm_delay_chain[i*4]),          // 进位输入(来自前一个CARRY4)
                .CYINIT(1'b0),                      // 不使用CYINIT
                .DI(4'b0000),                       // 数据输入(全0)
                .S(4'b1111)                         // 选择信号(全1确保进位传播)
            );

        end
    endgenerate

    // =========================================================================
    // 三级同步采样逻辑 - 减少亚稳态风险
    // =========================================================================

    always @(posedge clk_400m or negedge rst_n) begin
        if (!rst_n) begin
            sample_reg1 <= {TOTAL_STAGES{1'b0}};
            sample_reg2 <= {TOTAL_STAGES{1'b0}};
            sample_reg3 <= {TOTAL_STAGES{1'b0}};
            pwm_thermometer <= {TOTAL_STAGES{1'b0}};
            sampling_valid <= 1'b0;
            metastable_warning <= 1'b0;
        end else begin
            // 第一级采样 - 采样所有72级延迟
            sample_reg1 <= pwm_delay_chain[TOTAL_STAGES:1];

            // 第二级采样(进一步减少亚稳态)
            sample_reg2 <= sample_reg1;

            // 第三级采样(最终稳定)
            sample_reg3 <= sample_reg2;

            // 锁存最终结果
            pwm_thermometer <= sample_reg3;
            sampling_valid <= 1'b1;

            // 亚稳态检测
            metastable_warning <= (sample_reg1 != sample_reg2) ||
                                 (sample_reg2 != sample_reg3);
        end
    end

    // =========================================================================
    // 温度计码格式说明
    // =========================================================================
    //
    // 温度计码格式: [71:0]
    // - bit[71]: 第18个CARRY4的第4级(最慢)
    // - bit[68:70]: 第18个CARRY4的第1-3级
    // - bit[67:0]: 前17个CARRY4的所有级别
    //
    // CARRY4内部延迟级分布:
    // CARRY4_0: bit[3:0]   = {CO[3], CO[2], CO[1], CO[0]}
    // CARRY4_1: bit[7:4]   = {CO[3], CO[2], CO[1], CO[0]}
    // ...
    // CARRY4_17: bit[71:68] = {CO[3], CO[2], CO[1], CO[0]}
    //
    // 测量原理:
    // - 如果PWM脉宽为200ps，大约传播到第6级(200/35≈5.7)
    // - bit[5:0] = 6'b111111 (6个1)
    // - bit[71:6] = 66'b0...0 (其余为0)
    //

    // =========================================================================
    // 性能参数
    // =========================================================================
    //
    // 延迟链特性:
    // - CARRY4数量: 18个
    // - 总延迟级数: 72级
    // - 单级延迟: 35ps (典型值)
    // - 总延迟: 72 × 35ps = 2.52ns
    // - 理论分辨率: 35ps
    // - 最大测量脉宽: 2.52ns (纯延迟链测量)
    //
    // 采样特性:
    // - 采样时钟: 400MHz (2.5ns周期)
    // - 三级同步: 减少亚稳态风险
    // - 采样延迟: 3个时钟周期 = 7.5ns
    //

endmodule

// ===========================================================================
// 设计说明:
//
// 1. 为什么选择直接延迟链采样:
//    - 传统边沿检测无法测量<10ns脉宽
//    - 直接采样可以测量任意窄的脉宽
//    - 利用CARRY4的精确延迟特性
//
// 2. 三级同步设计:
//    - 减少亚稳态风险
//    - 提高采样可靠性
//    - 确保数据稳定性
//
// 3. 72级延迟链选择:
//    - 覆盖2.5ns测量范围
//    - 35ps理论分辨率
//    - 平衡精度和资源使用
//
// 4. 测量原理:
//    - PWM信号直接送入延迟链
//    - 在时钟边沿采样整个延迟链状态
//    - 通过1的个数计算脉宽
//    - 1的个数 × 35ps = 实际脉宽
//
// 5. 边界情况处理:
//    - 亚稳态检测: 监控三级采样的一致性
//    - 短脉宽: 可以精确测量到单级延迟(35ps)
//    - 长脉宽: 需要配合粗计数器使用
// ===========================================================================